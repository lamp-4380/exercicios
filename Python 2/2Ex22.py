d = float(input("Escreva um número entre 1 e 7: "))

if(d == 1):
    print("Esse número corresponde à Segunda-feira")
elif(d == 2):
    print("Esse número corresponde à Terça-feira")
elif(d == 3):
    print("Esse número corresponde à Quarta-feira")
elif(d == 4):
    print("Esse número corresponde à Quinta-feira")
elif(d == 5):
    print("Esse número corresponde à Sexta-feira")
elif(d == 6):
    print("Esse número corresponde à Sábado")
elif(d == 7):
    print("Esse número corresponde à Domingo")
else:
    print("Número inválido")